def get_remote_data(url, method="GET", rformat="xml", username=None, password=None, headers=None, body=None):
    log.debug('getting data from %s, using %s, and expecting %s.', url, method, rformat)
    http = httplib2.Http(timeout=15)
    http.force_exception_to_status_code = True

    url = "%s" % url
    
    if username is not None or password is not None:
        http.add_credentials(username, password)

    response, content = http.request(url, method, body, headers)

    if not response.status == 200:
        log.error('status code returned: %s', response.status)
        log.error('content recieved: %s' % content)
        return False

    if rformat in ("xml", "rss"):
        return element_tree.fromstring(content)
    elif rformat is "json":
        return simplejson.loads(content)

